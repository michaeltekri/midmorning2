fun main(){
    // Arithmetic operators
    var x=20
    var y= 5
    println(x + y)
    println(x * y)
    println(x / y)
    println(x - y)
    println(x % y) //Modulus

    // Comparison operators
    var a=67
    var b=30
    println(a < b)
    println(a > b)
    println(a <= b)
    println(a >= b)
    println(a == b)
    println(a != b)

    //Assignment operators
    var number=90
    println(number)

    number +=3
    println(number)

    //Logical operators -&&, !,||
    var num=10
    println(num >67 && num <20)
    println(num >67 || num <20)
    println(!(num >67 || num <20))

}